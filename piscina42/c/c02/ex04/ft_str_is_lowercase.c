int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
			return (0);
		i++;
	}
	return (1);
}

#include <unistd.h>

int main()
{
	char a[] = "";
	printf("%i", ft_str_is_lowercase(a));
}
