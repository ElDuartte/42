int     ft_strlen(char *str)
 {
	 int separator;

	 separator = 0;
	 while (str[separator] != '\0')
	 {
		 separator++;
	 }
	 return (separator);
}

int main()
{
	ft_strlen('1000000000000000000000000000000000000');
	return (0);
}
